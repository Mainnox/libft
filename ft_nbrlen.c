#include "includes/libft.h"

int		ft_nbrlen(unsigned long long n, char signe)
{
	int i;

	i = 0;
	if (signe == 1)
		i++;
	while (n > 10)
	{
		n = n / 10;
		i++;
	}
	return (i + 1);
}
